def target_language_name(language_code: str) -> str:
    code = (language_code or "").strip()
    if not code:
        return code

    lower = code.lower()
    if code in LANGUAGE_DISPLAY_NAMES:
        return LANGUAGE_DISPLAY_NAMES[code]
    if lower in LANGUAGE_DISPLAY_NAMES:
        return LANGUAGE_DISPLAY_NAMES[lower]

    parts = lower.split("_")
    base = LANGUAGE_DISPLAY_NAMES.get(parts[0], parts[0])
    script_or_region = [
        SCRIPT_DISPLAY_NAMES.get(part, part.replace("-", " ").title())
        for part in parts[1:]
    ]
    if script_or_region:
        return f"{base} ({', '.join(script_or_region)})"
    return base
