def load_json_dataset(file_path: str) -> list:
    """
    Load JSON format dataset
    
    Args:
        file_path: Dataset file path
    
    Returns:
        Data list
    """
    with open(file_path, 'r', encoding='utf-8') as f:
        data = json.load(f)
    
    # If data is in dictionary format, convert to list
    if isinstance(data, dict):
        # Assume each value in the dictionary is a list
        result = []
        for key, value in data.items():
            if isinstance(value, list):
                for item in value:
                    if isinstance(item, dict):
                        item['language'] = key
                    result.append(item)
        return result
    elif isinstance(data, list):
        return data
    else:
        raise ValueError(f"Unsupported data format in {file_path}")


def save_results(results: list, output_path: str):
    """
    Save evaluation results
    
    Args:
        results: Results list
        output_path: Output file path
    """
    import os
    os.makedirs(os.path.dirname(output_path) if os.path.dirname(output_path) else '.', exist_ok=True)
    
    with open(output_path, 'w', encoding='utf-8') as f:
        json.dump(results, f, ensure_ascii=False, indent=2)
