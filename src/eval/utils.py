def extract_math_output(text: str) -> Dict[str, str]:
    """
    Extract reasoning and answer from math problem output.
    Looks for <think>...</think> for reasoning and the last \\boxed{...} for the answer.
    Falls back to <answer>...</answer> if no \\boxed{} is found.
    """
    result = {"reasoning": "", "answer": ""}

    reasoning_match = re.search(r'<think>(.*?)</think>', text, re.DOTALL)
    if reasoning_match:
        result["reasoning"] = reasoning_match.group(1).strip()

    # Extract last \boxed{...}, handling one level of nested braces (e.g. \frac{1}{2})
    boxed_matches = re.findall(r'\\boxed\{((?:[^{}]|\{[^{}]*\})*)\}', text)
    if boxed_matches:
        result["answer"] = boxed_matches[-1].strip()
    else:
        answer_match = re.search(r'<answer>(.*?)</answer>', text, re.DOTALL)
        if answer_match:
            result["answer"] = answer_match.group(1).strip()

    return result


def load_json_dataset(file_path: str) -> list:
    """
    Load JSON format dataset
    
    Args:
        file_path: Dataset file path
    
    Returns:
        Data list
    """
    with open(file_path, 'r', encoding='utf-8') as f:
        data = json.load(f)
    
    # If data is in dictionary format, convert to list
    if isinstance(data, dict):
        # Assume each value in the dictionary is a list
        result = []
        for key, value in data.items():
            if isinstance(value, list):
                for item in value:
                    if isinstance(item, dict):
                        item['language'] = key
                    result.append(item)
        return result
    elif isinstance(data, list):
        return data
    else:
        raise ValueError(f"Unsupported data format in {file_path}")


def save_results(results: list, output_path: str):
    """
    Save evaluation results
    
    Args:
        results: Results list
        output_path: Output file path
    """
    import os
    os.makedirs(os.path.dirname(output_path) if os.path.dirname(output_path) else '.', exist_ok=True)
    
    with open(output_path, 'w', encoding='utf-8') as f:
        json.dump(results, f, ensure_ascii=False, indent=2)


def create_engine(model_config: ModelConfig) -> BaseEngine:
    """
    Create model engine based on configuration
    
    Args:
        model_config: Model configuration
    
    Returns:
        Model engine instance
    """
    if model_config.model_type.lower() == "qwen3":
        return Qwen3Engine(
            model_name=model_config.model_name,
            device_map=model_config.device_map,
            torch_dtype=model_config.torch_dtype,
            attn_implementation=model_config.attn_implementation,
            lora_path=model_config.lora_path
        )
    elif model_config.model_type.lower() == "llama":
        return LlamaEngine(
            model_name=model_config.model_name,
            device_map=model_config.device_map,
            torch_dtype=model_config.torch_dtype,
            attn_implementation=model_config.attn_implementation,
        )
    elif model_config.model_type.lower() == "mistral":
        return MistralEngine(
            model_name=model_config.model_name,
            device_map=model_config.device_map,
            torch_dtype=model_config.torch_dtype,
            attn_implementation=model_config.attn_implementation,
            lora_path=model_config.lora_path
        )
    elif model_config.model_type.lower() == "openai":
        return OpenAIEngine(
            model_name=model_config.model_name,
            api_key_env=model_config.api_key_env,
            base_url=model_config.base_url,
        )
    else:
        raise ValueError(f"Unsupported model type: {model_config.model_type}")


def run_reasoning(engine: BaseEngine, questions: str, languages: str = "", 
            model_config: Optional[ModelConfig] = None, eval_config: Optional[EvalConfig] = None,
            options: Optional[List[Dict[str, str]]] = None) -> Dict[str, str]:
    """
    Run Reasoning stage: Input target language question, output English CoT and answer
    
    Args:
        engine: Model engine
        question: Target language questions
        languages: Question languages (optional)
        model_config: Model configuration (optional)
        options: List of dicts with option_a/b/c/d for MC questions (optional)
    
    Returns:
        Dictionary containing English CoT and answer
    """
    is_math = eval_config is not None and eval_config.question_type == "math"

    # Build prompt — choose MC, math, or open-ended
    if options:
        prompts = [
            MC_REASONING_PROMPT.format(question=q, **_mc_format_kwargs(opts))
            for q, opts in zip(questions, options)
        ]
    elif is_math:
        prompts = [MATH_REASONING_PROMPT.format(question=q) for q in questions]
    else:
        prompts = [REASONING_PROMPT.format(question=question) for question in questions]

    # Generation parameters
    gen_kwargs = {}
    if model_config:
        gen_kwargs['max_new_tokens'] = model_config.max_new_tokens
        gen_kwargs['temperature'] = model_config.temperature
        gen_kwargs['top_p'] = model_config.top_p

    # Generate
    outputs = engine.generate_batch(prompts=prompts, **gen_kwargs)

    # Extract structured information
    if is_math:
        results = [extract_math_output(output) for output in outputs]
    else:
        results = [extract_reasoning_output(output) for output in outputs]
    return results


