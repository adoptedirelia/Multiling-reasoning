import re
import json
from typing import Dict, Optional, Tuple


class PromptBuilder:
    @staticmethod
    def build_mt1_prompt(question: str, context: str) -> str:
        return f"""
        You are a helpful assistant.
        Question: {question}
        Context: {context}
        Answer:
        """
    def build_mt2_prompt(question: str, context: str) -> str:
        return f"""
        You are a helpful assistant.
        Question: {question}
        Context: {context}
        Answer:
        """

def extract_mt1_output(text: str) -> Dict[str, str]:
    """
    Extract structured information from MT1 output
    
    Args:
        text: Text generated by MT1
    
    Returns:
        Dictionary containing question, reasoning, answer
    """
    result = {
        "question": "",
        "reasoning": "",
        "answer": ""
    }
    
    # Extract question (if exists)
    question_match = re.search(r'<question>(.*?)</question>', text, re.DOTALL)
    if question_match:
        result["question"] = question_match.group(1).strip()
    
    # Extract reasoning (support both think and redacted_reasoning tags, Qwen3 may generate think tag)
    # Try think tag first (Qwen3 may generate), then try redacted_reasoning tag
    reasoning_match = re.search(r'<think>(.*?)</think>', text, re.DOTALL)
    if not reasoning_match:
        reasoning_match = re.search(r'<think>(.*?)</think>', text, re.DOTALL)
    if reasoning_match:
        result["reasoning"] = reasoning_match.group(1).strip()
    
    # Extract answer
    answer_match = re.search(r'<answer>(.*?)</answer>', text, re.DOTALL)
    if answer_match:
        result["answer"] = answer_match.group(1).strip()
    
    return result


