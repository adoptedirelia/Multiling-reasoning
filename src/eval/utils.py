def detect_question_type(dataset: List[Dict]) -> str:
    """Auto-detect question type from dataset samples.
    Returns 'mc' if option_a/b/c/d exist, otherwise 'open_ended'."""
    if dataset and isinstance(dataset[0], dict) and "option_a" in dataset[0]:
        return "mc"
    return "open_ended"


def resolve_question_type(eval_config: EvalConfig, dataset: List[Dict]) -> str:
    """Resolve effective question type: use config value or auto-detect."""
    qt = eval_config.question_type.lower()
    if qt == "auto":
        qt = detect_question_type(dataset)
        print(f"Auto-detected question type: {qt}")
    return qt


