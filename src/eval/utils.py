def translate_questions_batch(engine: BaseEngine, questions: List[str], 
                              model_config: Optional[ModelConfig] = None,
                              batch_size: int = 1) -> List[str]:
    """
    Translate all questions from target language to English (MT1 step)
    
    Args:
        engine: MT1 translation engine
        questions: List of target language questions
        model_config: Model configuration (optional)
        batch_size: Batch size for translation
    
    Returns:
        List of English translated questions
    """
    english_questions = []
    
    # Generation parameters
    gen_kwargs = {}
    if model_config:
        gen_kwargs['max_new_tokens'] = model_config.max_new_tokens
        gen_kwargs['temperature'] = model_config.temperature
        gen_kwargs['top_p'] = model_config.top_p
    
    # Translate in batches
    for i in range(0, len(questions), batch_size):
        batch_questions = questions[i:i+batch_size]
        prompts = [MT1_PROMPT.format(question=q) for q in batch_questions]
        batch_outputs = engine.generate_batch(prompts=prompts, **gen_kwargs)
        results = [extract_translation_output(output) for output in batch_outputs]

        english_questions.extend([result["translation"] for result in results])
    
    return english_questions

def run_end_to_end(engine: BaseEngine, questions: List[str], languages: List[str] = None, 
                   model_config: Optional[ModelConfig] = None, eval_config: Optional[EvalConfig] = None) -> List[Dict[str, str]]:
    """
    Run end-to-end baseline: Input target language question, output target language reasoning and answer
    
    Args:
        engine: Model engine
        questions: Target language questions
        languages: Question languages
        model_config: Model configuration (optional)
        eval_config: Evaluation configuration (optional)
    
    Returns:
        List of dictionaries containing reasoning and answer
    """
    # Build end-to-end prompts
    prompts = []
    for i, question in enumerate(questions):
        language = languages[i] if languages and i < len(languages) else ""
        prompt = END_TO_END_PROMPT.format(question=question, language=language)
        prompts.append(prompt)
    
    # Generation parameters
    gen_kwargs = {}
    if model_config:
        gen_kwargs['max_new_tokens'] = model_config.max_new_tokens
        gen_kwargs['temperature'] = model_config.temperature
        gen_kwargs['top_p'] = model_config.top_p
    
    # Generate
    outputs = engine.generate_batch(prompts=prompts, **gen_kwargs)
    
    # Extract structured information
    results = [extract_mt2_output(output) for output in outputs]
    return results


def run_cascade_baseline(llm_engine: BaseEngine, mt2_engine: BaseEngine,
                         questions: List[str], english_questions: List[str],
                         languages: List[str] = None,
                         llm_config: Optional[ModelConfig] = None,
                         mt2_config: Optional[ModelConfig] = None,
                         eval_config: Optional[EvalConfig] = None) -> List[Dict[str, str]]:
    """
    Run cascade baseline: English questions -> LLM -> English output -> MT2 -> Output
    
    Args:
        llm_engine: LLM model engine for reasoning
        mt2_engine: MT2 model engine
        questions: Target language questions (original)
        english_questions: Pre-translated English questions
        languages: Question languages
        llm_config: LLM model configuration (optional)
        mt2_config: MT2 model configuration (optional)
        eval_config: Evaluation configuration (optional)
    
    Returns:
        List of dictionaries containing all intermediate and final results
    """
    # Step 1: LLM - Generate English reasoning and answer
    results = run_reasoning(llm_engine, english_questions, model_config=llm_config, eval_config=eval_config)
    
    # Step 2: MT2 - Translate back to target language (using MT2_BASE_PROMPT, only English question and answer)
    mt2_results = run_mt2_base(
        engine=mt2_engine, 
        questions=questions, 
        english_questions=english_questions,
        english_answers=[r.get("answer", "") for r in results],
        languages=languages,
        model_config=mt2_config,
        eval_config=eval_config
    )
    
    # Combine all results
    combined_results = []
    for i in range(len(questions)):
        combined_results.append({
            "english_question": english_questions[i],
            "llm_result": results[i],
            "mt2_result": mt2_results[i]
        })
    
    return combined_results


def run_prompting_baseline(llm_engine: BaseEngine, mt2_engine: BaseEngine,
                           questions: List[str], english_questions: List[str],
                           languages: List[str] = None,
                           llm_config: Optional[ModelConfig] = None,
                           mt2_config: Optional[ModelConfig] = None,
                           eval_config: Optional[EvalConfig] = None) -> List[Dict[str, str]]:
    """
    Run prompting baseline: English questions -> LLM -> (input, eng reasoning, eng output) -> MT2 -> Output
    
    Args:
        llm_engine: LLM model engine for reasoning
        mt2_engine: MT2 model engine
        questions: Target language questions (original)
        english_questions: Pre-translated English questions
        languages: Question languages
        llm_config: LLM model configuration (optional)
        mt2_config: MT2 model configuration (optional)
        eval_config: Evaluation configuration (optional)
    
    Returns:
        List of dictionaries containing all intermediate and final results
    """
    # Step 1: LLM - Generate English reasoning and answer
    results = run_reasoning(llm_engine, english_questions, model_config=llm_config, eval_config=eval_config)
    # Step 2: MT2 - Translate back to target language (using MT2_PROMPT, with English question, reasoning, and answer)
    mt2_results = run_mt2(
        engine=mt2_engine,
        questions=questions,
        english_questions=english_questions,
        english_thinking_processes=[r.get("reasoning", "") for r in results],
        english_answers=[r.get("answer", "") for r in results],
        languages=languages,
        model_config=mt2_config,
        eval_config=eval_config
    )
    
    # Combine all results
    combined_results = []
    for i in range(len(questions)):
        combined_results.append({
            "english_question": english_questions[i],
            "llm_result": results[i],
            "mt2_result": mt2_results[i]
        })
    
    return combined_results
