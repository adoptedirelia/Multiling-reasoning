def run_mt2_ablation(
    engine: BaseEngine,
    ablation_type: str,
    questions: List[str],
    english_questions: List[str],
    english_thinking_processes: List[str],
    english_answers: List[str],
    languages: List[str],
    model_config: Optional[ModelConfig] = None,
    eval_config: Optional[EvalConfig] = None,
    options: Optional[List[Dict[str, str]]] = None,
) -> List[Dict]:
    """
    Run MT2 step for ablation studies. ablation_type selects what context MT2 receives:
      'answer_only'     — English answer only (same as cascade MT2)
      'answer_orig_q'   — original question + English answer
      'answer_eng_q'    — English question + English answer
      'answer_reasoning'— English reasoning trace + English answer
    """
    prompts = []
    for i in range(len(questions)):
        opts = options[i] if options else None
        if ablation_type == "answer_only":
            if opts:
                prompt = MC_MT2_BASE_PROMPT.format(
                    English_Answer=english_answers[i],
                    language=languages[i],
                    **_mc_format_kwargs(opts),
                )
            else:
                prompt = MT2_BASE_PROMPT.format(
                    English_Answer=english_answers[i],
                    language=languages[i],
                )
        elif ablation_type == "answer_orig_q":
            if opts:
                prompt = MC_ABLATION_ORIG_Q_PROMPT.format(
                    question=questions[i],
                    English_Answer=english_answers[i],
                    language=languages[i],
                    **_mc_format_kwargs(opts),
                )
            else:
                prompt = ABLATION_ORIG_Q_PROMPT.format(
                    question=questions[i],
                    English_Answer=english_answers[i],
                    language=languages[i],
                )
        elif ablation_type == "answer_eng_q":
            if opts:
                prompt = MC_ABLATION_ENG_Q_PROMPT.format(
                    English_Question=english_questions[i],
                    English_Answer=english_answers[i],
                    language=languages[i],
                    **_mc_format_kwargs(opts),
                )
            else:
                prompt = ABLATION_ENG_Q_PROMPT.format(
                    English_Question=english_questions[i],
                    English_Answer=english_answers[i],
                    language=languages[i],
                )
        elif ablation_type == "answer_reasoning":
            if opts:
                prompt = MC_ABLATION_REASONING_PROMPT.format(
                    English_Thinking_Process=english_thinking_processes[i],
                    English_Answer=english_answers[i],
                    language=languages[i],
                    **_mc_format_kwargs(opts),
                )
            else:
                prompt = ABLATION_REASONING_PROMPT.format(
                    English_Thinking_Process=english_thinking_processes[i],
                    English_Answer=english_answers[i],
                    language=languages[i],
                )
        else:
            raise ValueError(f"Unknown ablation type: {ablation_type}")
        prompts.append(prompt)

    gen_kwargs = {}
    if model_config:
        gen_kwargs['max_new_tokens'] = model_config.max_new_tokens
        gen_kwargs['temperature'] = model_config.temperature
        gen_kwargs['top_p'] = model_config.top_p

    outputs = engine.generate_batch(prompts=prompts, **gen_kwargs)
    return [extract_mt2_output(output) for output in outputs]


def run_cascade_baseline_with_error(llm_engine: BaseEngine, mt2_engine: BaseEngine,
                         questions: List[str], english_questions: List[str],
                         languages: List[str] = None,
                         llm_config: Optional[ModelConfig] = None,
                         mt2_config: Optional[ModelConfig] = None,
                         eval_config: Optional[EvalConfig] = None,
                         mt1_result: Dict[str, str] = None,
                         options: Optional[List[Dict[str, str]]] = None) -> List[Dict[str, str]]:
    """
    Run cascade baseline with pre-computed LLM results.
    """
    results = [i['llm_result'] for i in mt1_result]
    
    mt2_results = run_mt2_base(
        engine=mt2_engine, 
        questions=questions, 
        english_questions=english_questions,
        english_answers=[r.get("answer", "") for r in results],
        languages=languages,
        model_config=mt2_config,
        eval_config=eval_config,
        options=options
    )
    
    combined_results = []
    for i in range(len(questions)):
        combined_results.append({
            "english_question": english_questions[i],
            "llm_result": results[i],
            "mt2_result": mt2_results[i]
        })
    
    return combined_results
