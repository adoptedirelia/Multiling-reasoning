def extract_translation_output(text: str) -> Dict[str, str]:
    """
    Extract structured information from Translation output
    
    Args:
        text: Text generated by Translation
    """
    result = {
        "translation": ""
    }
    
    # Extract translation
    translation_match = re.search(r'<translation>(.*?)</translation>', text, re.DOTALL)
    if translation_match:
        result["translation"] = translation_match.group(1).strip()
    return result

def extract_reasoning_output(text: str) -> Dict[str, str]:
    """
    Extract structured information from Reasoning output

    Args:
        text: Text generated by Reasoning
    
    Returns:
        Dictionary containing English CoT and answer
    """
    result = {
        "reasoning": "",
        "answer": ""
    }
    

    
    # Extract reasoning (support both think and redacted_reasoning tags, Qwen3 may generate think tag)
    # Try redacted_reasoning tag first, then try think tag
    reasoning_match = re.search(r'<think>(.*?)</think>', text, re.DOTALL)
    if not reasoning_match:
        reasoning_match = re.search(r'<think>(.*?)</think>', text, re.DOTALL)
    if reasoning_match:
        result["reasoning"] = reasoning_match.group(1).strip()
    
    # Extract answer
    answer_match = re.search(r'<answer>(.*?)</answer>', text, re.DOTALL)
    if answer_match:
        result["answer"] = answer_match.group(1).strip()
    
    return result


